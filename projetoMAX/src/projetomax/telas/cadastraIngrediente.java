    private void cadastraIngrediente(String nome, String tipo, double valorEnergetico, double carboidratos,double proteinas,double gordurasTotais,double gordurasSaturadas,double gordurasTrans,double fibraAlimentar,double sodios,ArrayList<Alimento>a){
        try{
        Alimento ali= new Ingrediente();
        ali.setNome(nome);
        ali.setTipo(tipo);
        ((Ingrediente) ali).setValorEnergetico(valorEnergetico);
        ((Ingrediente) ali).setCarboidratos(carboidratos);
        ((Ingrediente) ali).setProteinas(proteinas);
        ((Ingrediente) ali).setGordurasTotais(gordurasTotais);
        ((Ingrediente) ali).setGordurasSaturadas(gordurasSaturadas);
        ((Ingrediente) ali).setGordurasTrans(gordurasTrans);
        a.add(ali);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Coloque n√∫meros");
        }
    }
    
